
function myFunction1() {
    var element = document.getElementById("i1");
    var list = element.classList;
    if(list.contains("md-inactive")){
        list.remove("md-inactive");
        console.log(list);
    }else {
    element.classList.add("md-inactive");
    console.log(list);
    }
}

